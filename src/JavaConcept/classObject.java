package JavaConcept;

public class classObject {

	
//	Multiple Objects
	
	int x=5;
 	int y=10;
	
	public static void main(String[] args) {	
		
		classObject myobj1 = new classObject();
		classObject myobj2 = new classObject();
		
		System.out.println(myobj1.x);
		System.out.println(myobj2.y);


		
	}

}